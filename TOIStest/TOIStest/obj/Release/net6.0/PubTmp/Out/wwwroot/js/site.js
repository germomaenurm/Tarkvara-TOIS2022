var box_1 = document.querySelector("#ib_1");
var element1 = document.getElementById("info-1");
box_1.addEventListener("click", function () {
    if (element1.style.opacity == "0") {
        element1.style.display = "grid";
        element1.style.opacity = "1";
    } else {
        element1.style.display = "none";
        element1.style.opacity = "0";
    }
});
